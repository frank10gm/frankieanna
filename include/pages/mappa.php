    <h2>Piazza Nettuno</h2>
    <p>La fontana del Nettuno è una fontana monumentale che si trova a Bologna in piazza Nettuno (adiacente a Piazza Maggiore). Per via delle dimensioni della statua, i bolognesi la chiamano familiarmente "il Gigante" (al Żigànt in dialetto bolognese).</p>
    
