    <h2>Piazza Maggiore</h2>
    <p>Piazza Maggiore è la piazza principale di Bologna ed è circondata dai più importanti edifici della città medievale. Il più antico è il Palazzo del Podestà ed è sormontato dalla Torre dell'Arengo, che suonando la sua campana chiamava il popolo a raccolta.</p>
    
